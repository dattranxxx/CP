#include <bits/stdc++.h>
#define print(_v) for (auto &_ : _v) {cerr << _ << ' ';} cerr << endl;
using namespace std;
using ll = long long;
const int oo = 1e9;
const int N = 1500;
struct P {
  int x, y, s;
  P(int x = 0, int y = 0, int s = 0): x(x), y(y), s(s) {}
  bool operator < (const P& p) const {
    if (x == p.x) 
      return y < p.y;
    return x < p.x;
  }
  bool operator == (const P& p) const {
    return x == p.x && y == p.y;
  }
  friend ostream& operator << (ostream& os, const P& p) {
    return os << '[' << p.x << ", " << p.y << ", " << p.s << ']';
  }
} a[N], b[N-1];
int n;
int main() {
  cin.tie(0)->sync_with_stdio(0); cout.tie(0);
  cin >> n;
  for (int i = 0; i < n; ++i)
    cin >> a[i].x >> a[i].y >> a[i].s, 
    a[i].s = a[i].s * a[i].s + 5;
  int res = 0;
  sort(a, a + n);
  for (int i = 0; i < n; ++i) {
    int m = 0;
    for (int j = i + 1; j < n; ++j)
      b[m].x = a[j].x - a[i].x, 
      b[m].y = a[j].y - a[i].y,
      b[m].s = a[j].s, m++;
    for (int j = 0; j < m; ++j) {
      if (b[j].x == 0) {
        b[j].y = 1; continue;
      }
      if (b[j].y == 0) {
        b[j].x = 1; continue;
      }
      int g = __gcd(b[j].x, b[j].y);
      b[j].x /= g, b[j].y /= g;
    }
    sort(b, b + m);
    for (int j = 0, k, cur; j < m;) {
      k = j + 1, cur = a[i].s + b[j].s;
      while (k < n-1 && b[k] == b[j]) cur += b[k].s, k++;
      res = max(res, cur), j = k;
    }
  }
  cout << res;
  return 0;
}

