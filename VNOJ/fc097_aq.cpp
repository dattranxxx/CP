#include <bits/stdc++.h>
#define print(_v) for (auto &_ : _v) {cerr << _ << ' ';} cerr << endl;
using namespace std;
using ll = long long;
const int oo = 1e9;
const int N = 1e5;
struct fenwick_tree {
  int n;
  vector<int> bit;
  fenwick_tree(int n = 0): n(n), bit(n) {}
  void update(int i, int x) {
    for (; ~i; i = (i & (i + 1)) - 1)
      bit[i] += x;
  }
  int get(int i) {
    int res = 0;
    for (; i < n; i = i | (i + 1))
      res += bit[i];
    return res;
  }
};
int a[N];
int n, m;
int main() {
  cin.tie(0)->sync_with_stdio(0); cout.tie(0);
  cin >> n >> m;
  for (int i = 0; i < n; ++i) 
    cin >> a[i];
  sort(a, a + n);
  fenwick_tree bit(n);
  char c;
  int lo, hi, mid, ans, L, R;
  for (int i = 0, l, r; i < m; ++i) {
    cin >> c >> l >> r; // c h
    if (c == 'F') {
      lo = 0, hi = n-1, L = -1;
      while (lo <= hi) {
        mid = (lo + hi) >> 1;
        if (a[mid] + bit.get(mid) > r) {
          L = mid;
          hi = mid - 1;
        } else {
          lo = mid + 1;
        }
      }
      if (L != -1)
        bit.update(L, 1),
        bit.update(min(n-1, L + l), -1);
    } else {
      L = -1, R = -1;
      lo = 0, hi = n-1;
      while (lo <= hi) {
        mid = (lo + hi) >> 1;
        if (a[mid] + bit.get(mid) >= l) {
          L = mid;
          hi = mid - 1;
        } else {
          lo = mid + 1;
        }
      }
      lo = 0, hi = n-1;
      while (lo <= hi) {
        mid = (lo + hi) >> 1;
        if (a[mid] + bit.get(mid) <= r) {
          R = mid;
          lo = mid + 1;
        } else {
          hi = mid - 1;
        }
      }
      if (L == -1 || R == -1)
        cout << 0 << '\n';
      else
        cout << R - L + 1 << '\n';
    }
  }
  cin >> lo >> hi;
  return 0;
}

