#include <bits/stdc++.h>
#define print(_v) for (auto &_ : _v) {cerr << _ << ' ';} cerr << endl;
using namespace std;
using ll = long long;
const int oo = 1e9;
const int N = 400, M = 2.5e5;
struct S {
  int s, f, c, r;
  S(int s = 0, int f = 0, int c = 0, int r = 0): s(s), f(f), c(c), r(r) {}
  bool operator < (const S& s) const {
    return r < s.r;
  }
} q[M];
int a[N+1];
int dp[2][N+1][N+1];
int n, m, cur, pre;
int _;
void calc(int l, int r, int x, int y, int L) {
  if (l > r) return;
  pair<int, int> best = {oo, -1};
  int m = (l + r) >> 1;
  for (int i = x; i <= min(m, y); ++i)
    best = min(best, {max(dp[pre][L][i], a[m] - a[i]), i});
  dp[cur][L][m] = best.first;
  calc(l, m-1, x, best.second, L);
  //printf("dp %d %d %d = %d\n", _, L, m, dp[cur][L][m]);
  calc(m+1, r, best.second, y, L);
}
int main() {
  cin.tie(0)->sync_with_stdio(0); cout.tie(0);
  cin >> n >> m;
  for (int i = 1; i <= n; ++i)
    cin >> a[i];
  for (int i = 0; i < m; ++i)
    cin >> q[i].s >> q[i].f >> q[i].c >> q[i].r, q[i].r = min(n, q[i].r + 1);
  sort(q, q + m);
  // dp[l][r][k] = min(max(d[l][j][k-1]), a[r] - a[j]);
  memset(dp, 0x3f, sizeof(dp));
  pre = 0, cur = 1;
  int k = 0;
  ll res = 0;
  for (int j = 1; j <= n; ++j) {
    for (int L = 1; L <= n; ++L) {
      dp[pre][L][L] = 0;
      calc(L, n, L, n, L);
    }
    while (k < m && q[k].r == j) 
      res = max(res, ll(q[k].c) * dp[cur][q[k].s][q[k].f]), k++;
    swap(pre, cur);
  }
  cout << res;
  return 0;
}

