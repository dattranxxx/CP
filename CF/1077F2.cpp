#include <bits/stdc++.h>
#define print(_v) for (auto &_ : _v) {cerr << _ << ' ';} cerr << endl;
using namespace std;
using ll = long long;
const ll oo = 1e18;
const int N = 5000;
int a[N+1];
int n, k, m;
int main() {
  cin.tie(0)->sync_with_stdio(0); cout.tie(0);
  cin >> n >> k >> m;
  for (int i = 1; i <= n; ++i)
    cin >> a[i];
  // dp[i][j] = la so cach chon j so tinh ca ai xet i so dau tien lon nhat tm
  // -> dp[i][j] = max(dp[i-1][j-1], dp[i-2][j-1],.., dp[i-k][j-1]) + a[i];
  deque<int> q;
  vector<ll> pre(n + 1, -oo), cur(n + 1);
  ll res = -1;
  for (int j = 1; j <= m; ++j) {
    q = deque<int>();
    pre[0] = 0;
    q.emplace_back(0);
    for (int i = 1; i <= n; ++i) {
      if (!q.empty() && q.front() < i-k) 
        q.pop_front();
      if (!q.empty())
        cur[i] = pre[q.front()] + a[i];
      while (!q.empty() && pre[i] >= pre[q.back()]) 
        q.pop_back();
      q.emplace_back(i);
      if (j == m && i >= n - k + 1)
        res = max(res, cur[i]);
    }
    cur.swap(pre);
  }
  cout << res;
  return 0;
}

